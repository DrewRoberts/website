    <section id="content">
      <p>Now that the 2018 <a href="http://bleacherreport.com/nfl">NFL</a> draft is over, the focus for teams shifts from evaluating prospects to putting them to use during offseason programs.</p>
      <p>Over the next few months, the 32 NFL franchises will try to put their respective draft classes in position to succeed next year, especially the future stars taken at the top of the first round.</p>
      <p>All we can do before the games are played in the fall is pick out which players were best fits for each team and grade them as such.</p>
      <p>On paper, teams like the New England Patriots and Cleveland Browns came out as winners, while the Kansas City Chiefs and Oakland Raiders didn't fare as well.</p>
      <div class="ad-container">
        <amp-ad data-slot="/8663477/BR/NFL/article" height="250" json='{"targeting":{"pg":"amp","pos":"rect_atf_01","app":"false","social":"false","layout":"default","cnn":"false","victory":"none","appname":"googleamp","zone":"article","writer":"Joe Tansey","TEAM":"none","tags":"NFL,Bill_Belichick,Preview_Prediction,Sony_Michel,Josh_Rosen","tag_id":16,"site":"NFL","render_strategy":"article","league":"NFL","gp_flag":"","fantasy":"none","EVENT":"none","DIVISION":"none","buzz":"","ARTICLE":2773377}}' type="doubleclick" width="300"></amp-ad>
      </div>
      <p>While the grades hold some standing after the draft, there's always a possibility they can change over the next 12 months once the 256 players taken in the 2018 draft put the pads on and get acquainted with life in the NFL.</p>
      <p>    </p>
      <p><span><strong>NFL Draft Tracker </strong></span></p>
