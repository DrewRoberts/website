      <p><span><strong>Analysis</strong></span></p>
      <p><strong>New England Patriots <br></strong></p>
      <p>The Patriots were one of the busiest teams during the three-day draft process between the nine picks and eight trades they made.</p>
      <p>New England took Georgia teammates offensive lineman Isaiah Wynn and running back Sony Michel with its pair of first-round picks, the first of which came in—you guessed it—a trade, but that one took place a few weeks ago as wide receiver Brandin Cooks moved to the Los Angeles Rams.</p>
      <figure class="image"><amp-img src="https://img.bleacherreport.net/img/article/media_slots/photos/002/798/138/65fab2b55bb4ee03d6b07e9b72f5cc2c_crop_exact.jpg?w=2975&amp;h=2048&amp;q=85" layout="responsive" width="1487" height="1024"></amp-img><figcaption><span class="credit">Doug Benc/Associated Press</span></figcaption></figure>
      <p><a href="http://bleacherreport.com/bill-belichick">Bill Belichick</a>'s team picked up a solid corner in Florida's Duke Dawson in the second round before not selecting again until the fifth round.</p>
      <div class="ad-container">
        <amp-ad data-slot="/8663477/BR/NFL/article" height="250" json='{"targeting":{"pg":"amp","pos":"rect_atf_02","app":"false","social":"false","layout":"default","cnn":"false","victory":"none","appname":"googleamp","zone":"article","writer":"Joe Tansey","TEAM":"none","tags":"NFL,Bill_Belichick,Preview_Prediction,Sony_Michel,Josh_Rosen","tag_id":16,"site":"NFL","render_strategy":"article","league":"NFL","gp_flag":"","fantasy":"none","EVENT":"none","DIVISION":"none","buzz":"","ARTICLE":2773377}}' type="doubleclick" width="300"></amp-ad>
      </div>
      <p>The Patriots made deals to acquire Chicago's second-round pick in 2019 and Detroit's third-round selection next year to set up another successful draft in 12 months' time.</p>
      <p>The most notable names of the last six picks the Patriots made were wide receiver Braxton Berrios out of Miami and quarterback Danny Etling from LSU.</p>
      <p>On first glance, Berrios appears to be the perfect fit for the slot role that the Patriots value so much in their offensive scheme.</p>
      <p>With three solid players taken in the first two rounds, and value gained throughout the rest of the selection process, it's hard to argue the Patriots had a bad weekend.</p>
      <p><strong>     </strong></p>
      <p><strong>Arizona Cardinals </strong></p>
      <p>Arizona entered Thursday as one of the teams in contention for a quarterback, but how it would acquire its new signal-caller remained to be seen. </p>
      <p>The Cardinals traded up five spots to No. 10 in order to select Josh Rosen out of UCLA, and with the pick, they gained a hungry quarterback ready to prove the nine teams that passed on him wrong.</p>
      <p>Rosen enters an ideal situation in head coach Steve Wilks' first season, as the pressure won't be on the rookie to start right away.</p>
      <figure class="image"><amp-img src="https://img.bleacherreport.net/img/article/media_slots/photos/002/798/140/15e75bfcb761cc8334d8cdc8c453d2fa_crop_exact.jpg?w=2975&amp;h=2048&amp;q=85" layout="responsive" width="1059" height="706"></amp-img><figcaption><span class="credit">Rick Scuteri/Associated Press</span></figcaption></figure>
      <p>The ideal scenario for the Cardinals is to bring Rosen along with <a href="http://bleacherreport.com/sam-bradford">Sam Bradford</a> beginning the season as No. 1 on the depth chart.</p>
      <p>Of course, the Cardinals need a little bit of luck to keep Bradford healthy throughout the year, but if he does, it creates a buffer for Rosen to prepare properly before taking over at some point.</p>
      <div class="ad-container">
        <amp-ad data-slot="/8663477/BR/NFL/article" height="250" json='{"targeting":{"pg":"amp","pos":"rect_atf_03","app":"false","social":"false","layout":"default","cnn":"false","victory":"none","appname":"googleamp","zone":"article","writer":"Joe Tansey","TEAM":"none","tags":"NFL,Bill_Belichick,Preview_Prediction,Sony_Michel,Josh_Rosen","tag_id":16,"site":"NFL","render_strategy":"article","league":"NFL","gp_flag":"","fantasy":"none","EVENT":"none","DIVISION":"none","buzz":"","ARTICLE":2773377}}' type="doubleclick" width="300"></amp-ad>
      </div>
      <p>Arizona picked up an offensive weapon for Rosen in the second round in the form of Christian Kirk from Texas A&amp;M, who should immediately help the Cardinals special teams unit.</p>
      <p>By adding two key players on offense, the Cardinals improved and set their first-year head coach up for success.</p>
      <p>Center Mason Cole was a solid third-round selection, as the Cardinals did the best they could with six picks.</p>
      <p>Although they didn't end up with the volume of prospects some teams did, the Cardinals got value out of theirs and improved positions of need, which is all you can ask for out of a draft.</p>
      <p><strong>     </strong></p>
      <p><strong>Kansas City Chiefs </strong></p>
      <p>Kansas City was in a difficult position from the start, as it traded away its 2018 first-round pick to Buffalo a year ago to choose quarterback Patrick Mahomes.</p>
      <p>When the Chiefs finally got on the clock Friday, they brought in three defensive prospects, starting with second-round pick Breeland Speaks, a defensive end from Ole Miss.</p>
      <figure class="image"><amp-img src="https://img.bleacherreport.net/img/article/media_slots/photos/002/798/141/3cf7622b763e4356aaed34f623c59427_crop_exact.jpg?w=2975&amp;h=2048&amp;q=85" layout="responsive" width="1487" height="1024"></amp-img><figcaption><span class="credit">Thomas Graning/Associated Press</span></figcaption></figure>
      <p>The team's defensive approach to the draft continued into Day 3, as all six of the players taken by the Chiefs play on that side of the ball.</p>
      <p>Although the Chiefs have a loaded offense with running back Kareem Hunt, wide receiver Tyreek Hill and tight end Travis Kelce at the forefront, it would've been nice to see them add a bit more depth with so many offensive skill players available at the front end of the second round.</p>
      <p>Kansas City certainly addressed what it thought in its mind was its biggest needs, but there's no standout star on the team's list of six selections.</p>
      <div class="ad-container">
        <amp-ad data-slot="/8663477/BR/NFL/article" height="250" json='{"targeting":{"pg":"amp","pos":"rect_atf_03","app":"false","social":"false","layout":"default","cnn":"false","victory":"none","appname":"googleamp","zone":"article","writer":"Joe Tansey","TEAM":"none","tags":"NFL,Bill_Belichick,Preview_Prediction,Sony_Michel,Josh_Rosen","tag_id":16,"site":"NFL","render_strategy":"article","league":"NFL","gp_flag":"","fantasy":"none","EVENT":"none","DIVISION":"none","buzz":"","ARTICLE":2773377}}' type="doubleclick" width="300"></amp-ad>
      </div>
      <p>Giving the Chiefs a low grade could turn out to be the wrong call down the line, but right now their draft class feels a little underwhelming.</p>
