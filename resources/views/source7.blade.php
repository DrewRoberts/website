<p>So, what exactly is this hardware then, if not a fully new Xbox? The most common theory is that it might mark the debut of the new Xbox Elite controller, glimpses of which have leaked already, and it might be time to show it off for good.</p>
<p>Past that, however, when addressing the future of Xbox, Phil Spencer has made comments that the future of the brand was possibly a <em>family</em> of devices, rather than just one central box. No one really understood what he meant by that at the time, but it <em>could</em> be possible we might be seeing something having to do with that at Gamescom.</p>
<p>
 <!--nextpage-->
<br /> Xbox needs a win in 2018. It had a well-orchestrated show at E3 with a lot of big acquisition announcements, but in reality, its year has been barren. <em>Sea of Thieves</em> has been its highest profile release, and that was met with…mixed reactions at best. <em>Crackdown 3</em> continues to look troubled and has been delayed out of 2018 entirely. Once again, the console’s lone saving grace is <em>Forza</em>, with <em>Forza Horizon 4 </em>coming this October.</p>
<p>A big announcement of something significant at Gamescom would be just what the doctor ordered, but we’ll have to see what they have planned. I know a lot of people love the Xbox Elite controller, so that would excite the base, but probably wouldn’t do all that much for outsiders who aren’t going to shell out for the pricey accessory.</p>
<p>
 <amp-ad width="321" height="251" type="doubleclick" data-block-on-consent="" data-slot="/7175/fdcmobile/amp/article" data-multi-size="300x250,320x50,300x50,1x1,300x251" data-multi-size-validation="false" rtc-config="{&quot;vendors&quot;: {&quot;medianet&quot;: {&quot;CID&quot;: &quot;8CU66O230&quot;}}}" json="{&quot;targeting&quot;:{&quot;displaysection&quot;:&quot;games&quot;,&quot;ns&quot;:&quot;microsoft&quot;,&quot;amp&quot;:&quot;true&quot;,&quot;fl&quot;:&quot;&quot;,&quot;author&quot;:&quot;paultassi&quot;,&quot;channel&quot;:&quot;innovation,consumer&quot;,&quot;section&quot;:&quot;innovation:games,consumer:hollywood&amp;entertainment&quot;,&quot;type&quot;:&quot;contributor&quot;,&quot;mobilex&quot;:&quot;2&quot;,&quot;pos&quot;:&quot;mobilex&quot;,&quot;templatetype&quot;:&quot;standard&quot;,&quot;swimlane&quot;:&quot;&quot;,&quot;specialslot&quot;:&quot;&quot;,&quot;id&quot;:&quot;blogAndPostId/blog/post/1174-42801&quot;,&quot;brand&quot;:&quot;&quot;,&quot;strnativekey&quot;:&quot;twzGG8q5aVPXGmiCccciYeT7&quot;,&quot;displaychannel&quot;:&quot;technology&quot;}}">
