<p>Microsoft is heading to Gamescom in Germany next month, and they will be bringing at least one significant announcement with them. In an era where console generations are practically a decade long, any talk about new hardware is enough to make ears perk up, and that’s what Microsoft says is coming.</p>
<p>
 <amp-ad width="321" height="251" type="doubleclick" data-block-on-consent="" data-slot="/7175/fdcmobile/amp/article" data-multi-size="300x250,320x50,300x50,1x1,300x251" data-multi-size-validation="false" rtc-config="{&quot;vendors&quot;: {&quot;medianet&quot;: {&quot;CID&quot;: &quot;8CU66O230&quot;}}}" json="{&quot;targeting&quot;:{&quot;displaysection&quot;:&quot;games&quot;,&quot;ns&quot;:&quot;microsoft&quot;,&quot;amp&quot;:&quot;true&quot;,&quot;fl&quot;:&quot;&quot;,&quot;author&quot;:&quot;paultassi&quot;,&quot;channel&quot;:&quot;innovation,consumer&quot;,&quot;section&quot;:&quot;innovation:games,consumer:hollywood&amp;entertainment&quot;,&quot;type&quot;:&quot;contributor&quot;,&quot;mobilex&quot;:&quot;0&quot;,&quot;pos&quot;:&quot;mobilerec&quot;,&quot;templatetype&quot;:&quot;standard&quot;,&quot;swimlane&quot;:&quot;&quot;,&quot;specialslot&quot;:&quot;&quot;,&quot;id&quot;:&quot;blogAndPostId/blog/post/1174-42801&quot;,&quot;brand&quot;:&quot;&quot;,&quot;displaychannel&quot;:&quot;technology&quot;}}">
</amp-ad>
</p>
<p>Microsoft <a href="https://majornelson.com/2018/07/18/xbox-is-coming-to-gamescom/" target="_blank" data-ga-track="ExternalLink:https://majornelson.com/2018/07/18/xbox-is-coming-to-gamescom/" rel="nofollow">published a blog post this morning</a> detailing its Gamescom plans, including the following:</p>
<blockquote>
 <p>“Tune in on Tuesday, August 21, at 4:30 p.m. CEST (7:30 a.m. PDT) for lots of news, all-new Xbox hardware and accessories, and features on upcoming titles that we can’t wait to tell you more about and perhaps even a few surprises!”</p>
</blockquote>
<p>So, are we about to see Xbox Two for the first time?</p>
<p>Almost certainly not.</p>
<p>
 <amp-ad width="320" height="120" type="doubleclick" data-block-on-consent="" data-slot="/7175/fdcmobile/amp/article" json="{&quot;targeting&quot;:{&quot;pos&quot;:&quot;ntv-amp&quot;,&quot;strnativekey&quot;:&quot;5Rwj6ekDKqm6DKeT6wwV7tiU&quot;}}">
</amp-ad>
</p>
<p>Unfortunately, even with the promise of new hardware, it does not seem likely that Microsoft is going to announce its next generation Xbox in August 2018 at Gamescom. Rumor has it we won’t see a new Xbox until 2020, and you can bet that when that is announced, Microsoft will have a dedicated event for it like they did with Project Scorpio.</p>
