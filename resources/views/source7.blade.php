<p>
 <amp-ad width="321" height="251" type="doubleclick" data-block-on-consent="" data-slot="/7175/fdcmobile/amp/article" data-multi-size="300x250,320x50,300x50,1x1,300x251" data-multi-size-validation="false" rtc-config="{&quot;vendors&quot;: {&quot;medianet&quot;: {&quot;CID&quot;: &quot;8CU66O230&quot;}}}" json="{&quot;targeting&quot;:{&quot;displaysection&quot;:&quot;games&quot;,&quot;ns&quot;:&quot;microsoft&quot;,&quot;amp&quot;:&quot;true&quot;,&quot;fl&quot;:&quot;&quot;,&quot;author&quot;:&quot;paultassi&quot;,&quot;channel&quot;:&quot;innovation,consumer&quot;,&quot;section&quot;:&quot;innovation:games,consumer:hollywood&amp;entertainment&quot;,&quot;type&quot;:&quot;contributor&quot;,&quot;mobilex&quot;:&quot;1&quot;,&quot;pos&quot;:&quot;mobilex&quot;,&quot;templatetype&quot;:&quot;standard&quot;,&quot;swimlane&quot;:&quot;&quot;,&quot;specialslot&quot;:&quot;&quot;,&quot;id&quot;:&quot;blogAndPostId/blog/post/1174-42801&quot;,&quot;brand&quot;:&quot;&quot;,&quot;strnativekey&quot;:&quot;twzGG8q5aVPXGmiCccciYeT7&quot;,&quot;displaychannel&quot;:&quot;technology&quot;}}">
